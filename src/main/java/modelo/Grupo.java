package modelo;

public class Grupo {
    private int id_grupo;
    private int numero_integrantes_grupo;
    private int id_barrio;

    public int getId_grupo() {
        return id_grupo;
    }

    public void setId_grupo(int id_grupo) {
        this.id_grupo = id_grupo;
    }

    public int getNumero_integrantes_grupo() {
        return numero_integrantes_grupo;
    }

    public void setNumero_integrantes_grupo(int numero_integrantes_grupo) {
        this.numero_integrantes_grupo = numero_integrantes_grupo;
    }

    public int getId_barrio() {
        return id_barrio;
    }

    public void setId_barrio(int id_barrio) {
        this.id_barrio = id_barrio;
    }
}
