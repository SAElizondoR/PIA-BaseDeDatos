package modelo;

public class Regalo {
    private int id_regalo;
    private String nombre_regalo;

    public int getId_regalo() {
        return id_regalo;
    }

    public void setId_regalo(int id_regalo) {
        this.id_regalo = id_regalo;
    }

    public String getNombre_regalo() {
        return nombre_regalo;
    }

    public void setNombre_regalo(String nombre_regalo) {
        this.nombre_regalo = nombre_regalo;
    }
}
